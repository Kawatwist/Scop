#include <unistd.h>

void	ft_putnbr(long long int n)
{
	char c;

	if (n >= -9 && n <= 9)
	{
		if (n < 0)
		{
			write(1, "-", 1);
			n *= -1;
		}
		c = n + 48;
		write(1, &c, 1);
		return ;
	}
	ft_putnbr(n / 10);
	if (n > 0)
		c = n % 10 + 48;
	else
		c = (-(n % 10) + 48);
	write(1, &c, 1);
}
